const template = (titolo, corpo, data, img, index) => {
    if (corpo == null) {
        return `
            <div class="timeline-block timeline-block-icon-only" id="timelineid-${data}">
                <div class="timeline-icon timeline-icon-text timeline-icon-hide-border">
                    <span>${titolo}</span>
                </div>
                <div class="timeline-content"></div>
            </div>
        `;
    }

    return `<div class="timeline-block" id="timelineid-${index}">
                        <div class="timeline-icon"></div>
                        <div class="timeline-content">
                            <h2>${titolo}</h2>
                            <p>${corpo}

                            ${img ? `<p><img src="${img}"/></p>` : ""}
                            
                            </p>
                            <div class="timeline-date">${data}</div>
                        </div>
                    </div>`;
};

document.addEventListener("DOMContentLoaded", () => {
    let timeline = document.getElementById("timeline");
    DATA.forEach((v, index) => {
        let node = document
            .createRange()
            .createContextualFragment(template(...v, index));
        timeline.appendChild(node);
    });
});
